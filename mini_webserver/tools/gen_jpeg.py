#!/usr/bin/env python3
"""Generate a minimal valid 1x1 white JPEG for testing."""
import struct

# SOI
jpeg = b'\xff\xd8'
# APP0
jpeg += b'\xff\xe0\x00\x10\x4a\x46\x49\x46\x00\x01\x01\x00\x00\x01\x00\x01\x00\x00'
# DQT
dqt = bytes([0xff, 0xdb, 0x00, 0x43, 0x00]) + bytes([16]*64)
jpeg += dqt
# SOF0 (1x1 grayscale)
jpeg += bytes([0xff, 0xc0, 0x00, 0x0b, 0x08, 0x00, 0x01, 0x00, 0x01, 0x01, 0x01, 0x11, 0x00])
# DHT
jpeg += bytes([0xff, 0xc4, 0x00, 0x1f, 0x00, 0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01,
               0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
               0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b])
# SOS
jpeg += bytes([0xff, 0xda, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3f, 0x00, 0x7f])
# EOI
jpeg += b'\xff\xd9'

with open('/home/wamdus/shixun/mini_webserver/www/img/logo.jpeg', 'wb') as f:
    f.write(jpeg)
print(f'logo.jpeg created: {len(jpeg)} bytes')
